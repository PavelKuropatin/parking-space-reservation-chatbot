-- working_hours
CREATE TABLE working_hours (
    hours_id        INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    day_of_week     TEXT,
    opens_at        TIME,
    closes_at       TIME,
    is_24h          BOOLEAN  NOT NULL DEFAULT FALSE,
    is_closed       BOOLEAN  NOT NULL DEFAULT FALSE,
    CONSTRAINT unique_working_hours UNIQUE (day_of_week),
    CONSTRAINT check_hours_not_both CHECK (NOT (is_24h AND is_closed)),
    CONSTRAINT check_hours_times CHECK (
        (is_24h OR is_closed) AND opens_at IS NULL AND closes_at IS NULL
        OR
        (NOT is_24h AND NOT is_closed AND opens_at IS NOT NULL AND closes_at IS NOT NULL)
    )
);
COMMENT ON COLUMN working_hours.hours_id IS 'Surrogate primary key for the hours row';
COMMENT ON COLUMN working_hours.day_of_week IS 'ISO-8601 weekday: 1=Monday ... 7=Sunday';
COMMENT ON COLUMN working_hours.opens_at IS 'Opening time; NULL when is_24h or is_closed';
COMMENT ON COLUMN working_hours.closes_at IS 'Closing time; NULL when is_24h or is_closed';
COMMENT ON COLUMN working_hours.is_24h IS 'TRUE if open around the clock on this day';
COMMENT ON COLUMN working_hours.is_closed IS 'TRUE if fully closed on this day';


CREATE OR REPLACE VIEW vw_working_hours
AS
SELECT  wh.hours_id,
        wh.day_of_week, 
        CASE 
            WHEN wh.is_closed then null
            WHEN wh.is_24h THEN '00:00'
            ELSE wh.opens_at
        END as opens_at,
        CASE 
            WHEN wh.is_closed then null
            WHEN wh.is_24h THEN '23:59'
            ELSE wh.closes_at
        END as closes_at,
        wh.is_closed as temporaly_closed
FROM  working_hours wh
ORDER BY wh.day_of_week;


