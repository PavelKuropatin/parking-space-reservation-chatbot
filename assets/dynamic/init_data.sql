-- locations — reference table; one row per physical parking facility.
CREATE TABLE locations (
    location_id     INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    location_code   VARCHAR(20)  NOT NULL UNIQUE,
    name            VARCHAR(120) NOT NULL,
    city            VARCHAR(80)  NOT NULL,
    timezone        VARCHAR(40)  NOT NULL DEFAULT 'Europe/Minsk',
    total_spaces    INT          NOT NULL CHECK (total_spaces >= 0),
    is_active       BOOLEAN      NOT NULL DEFAULT TRUE
);
COMMENT ON COLUMN locations.location_id IS 'Surrogate primary key for the facility';
COMMENT ON COLUMN locations.location_code IS 'Stable human/machine code (e.g. "CP-CENTRAL") used in URLs and joins';
COMMENT ON COLUMN locations.name IS 'Display name of the facility (e.g. "CityPark Central")';
COMMENT ON COLUMN locations.city IS 'City the facility is located in';
COMMENT ON COLUMN locations.timezone IS 'IANA timezone used to interpret opening hours and timestamps';
COMMENT ON COLUMN locations.total_spaces IS 'Total number of physical spaces across all levels';
COMMENT ON COLUMN locations.is_active IS 'Whether the facility is currently operational and bookable';


-- space_types — reference catalogue of bookable space categories.
CREATE TABLE space_types (
    space_type_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code          VARCHAR(20) NOT NULL UNIQUE,
    name          VARCHAR(60) NOT NULL,
    description   TEXT,
    is_active     BOOLEAN NOT NULL DEFAULT TRUE
);
COMMENT ON COLUMN space_types.space_type_id IS 'Surrogate primary key for the space type';
COMMENT ON COLUMN space_types.code IS 'Stable machine code (STANDARD, COMPACT, OVERSIZED, EV, ACCESSIBLE)';
COMMENT ON COLUMN space_types.name IS 'Human-readable label shown in the UI';
COMMENT ON COLUMN space_types.description IS 'Short description of what the space type offers';
COMMENT ON COLUMN space_types.is_active IS 'Whether this type is offered system-wide';


-- ---------------------------------------------------------------------
-- pricing — rate per location + space type, versioned by validity window.
-- ---------------------------------------------------------------------
CREATE TABLE pricing (
    price_id       INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    location_id    INT NOT NULL REFERENCES locations(location_id) ON DELETE CASCADE,
    space_type_id  INT NOT NULL REFERENCES space_types(space_type_id) ON DELETE CASCADE,
    hourly_rate    NUMERIC(8,2) NOT NULL CHECK (hourly_rate >= 0),
    daily_max_rate NUMERIC(8,2) CHECK (daily_max_rate >= 0),
    currency       CHAR(3) NOT NULL DEFAULT 'EUR',
    vat_rate       NUMERIC(5,2) NOT NULL DEFAULT 21.00
);
COMMENT ON COLUMN pricing.price_id IS 'Surrogate primary key for the price record';
COMMENT ON COLUMN pricing.location_id IS 'FK -> locations; facility this price applies to';
COMMENT ON COLUMN pricing.space_type_id IS 'FK -> space_types; space type this price applies to';
COMMENT ON COLUMN pricing.hourly_rate IS 'Price charged per hour, in `currency`';
COMMENT ON COLUMN pricing.daily_max_rate IS 'Daily cap (max charge per 24h); NULL = no cap';
COMMENT ON COLUMN pricing.currency IS 'ISO-4217 currency code for the rates';
COMMENT ON COLUMN pricing.vat_rate IS 'VAT percentage already included in the rates';


-- space_availability — current free-space snapshot per location + type.
CREATE TABLE space_availability (
    availability_id  INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    location_id      INT NOT NULL REFERENCES locations(location_id) ON DELETE CASCADE,
    space_type_id    INT NOT NULL REFERENCES space_types(space_type_id) ON DELETE CASCADE,
    total_spaces     INT NOT NULL CHECK (total_spaces >= 0),
    available_spaces INT NOT NULL CHECK (available_spaces >= 0),
    CONSTRAINT check_avail_le_total CHECK (available_spaces <= total_spaces),
    CONSTRAINT unique_availability UNIQUE (location_id, space_type_id)
);
COMMENT ON COLUMN space_availability.availability_id IS 'Surrogate primary key for the snapshot row';
COMMENT ON COLUMN space_availability.location_id IS 'FK -> locations; facility being measured';
COMMENT ON COLUMN space_availability.space_type_id IS 'FK -> space_types; space type being measured';
COMMENT ON COLUMN space_availability.total_spaces IS 'Total spaces of this type at this location';
COMMENT ON COLUMN space_availability.available_spaces IS 'Spaces of this type currently free to book';
COMMENT ON CONSTRAINT check_avail_le_total ON space_availability IS 'Free count can never exceed the total';
COMMENT ON CONSTRAINT unique_availability ON space_availability IS 'One current snapshot per location + space type';


-- working_hours — regular weekly opening hours per location.
-- one row per location per ISO weekday.
CREATE TABLE working_hours (
    hours_id        INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    location_id     INT      NOT NULL REFERENCES locations(location_id) ON DELETE CASCADE,
    day_of_week     SMALLINT NOT NULL CHECK (day_of_week BETWEEN 1 AND 7),
    opens_at        TIME,
    closes_at       TIME,
    is_24h          BOOLEAN  NOT NULL DEFAULT FALSE,
    is_closed       BOOLEAN  NOT NULL DEFAULT FALSE,
    CONSTRAINT unique_working_hours UNIQUE (location_id, day_of_week),
    CONSTRAINT check_hours_not_both CHECK (NOT (is_24h AND is_closed)),
    CONSTRAINT check_hours_times CHECK (
        (is_24h OR is_closed) AND opens_at IS NULL AND closes_at IS NULL
        OR
        (NOT is_24h AND NOT is_closed AND opens_at IS NOT NULL AND closes_at IS NOT NULL)
    )
);
COMMENT ON COLUMN working_hours.hours_id IS 'Surrogate primary key for the hours row';
COMMENT ON COLUMN working_hours.location_id IS 'FK -> locations; facility these hours belong to';
COMMENT ON COLUMN working_hours.day_of_week IS 'ISO-8601 weekday: 1=Monday ... 7=Sunday';
COMMENT ON COLUMN working_hours.opens_at IS 'Opening time; NULL when is_24h or is_closed';
COMMENT ON COLUMN working_hours.closes_at IS 'Closing time; NULL when is_24h or is_closed';
COMMENT ON COLUMN working_hours.is_24h IS 'TRUE if open around the clock on this day';
COMMENT ON COLUMN working_hours.is_closed IS 'TRUE if fully closed on this day';
COMMENT ON CONSTRAINT unique_working_hours ON working_hours IS 'Times required only for bounded days';
COMMENT ON CONSTRAINT check_hours_not_both ON working_hours IS 'A day cannot be both 24h and closed';
COMMENT ON CONSTRAINT check_hours_times ON working_hours IS 'Mixed time configuration';


-- special_hours — date-specific overrides (holidays, events, maintenance).
CREATE TABLE special_hours (
    special_id    INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    location_id   INT NOT NULL REFERENCES locations(location_id) ON DELETE CASCADE,
    calendar_date DATE NOT NULL,
    opens_at      TIME,
    closes_at     TIME,
    is_closed     BOOLEAN NOT NULL DEFAULT FALSE,
    description   VARCHAR(120),
    CONSTRAINT unique_special_hours UNIQUE (location_id, calendar_date),
    CONSTRAINT check_special_times CHECK (
        is_closed AND opens_at IS NULL AND closes_at IS NULL
        OR
        (NOT is_closed AND opens_at IS NOT NULL AND closes_at IS NOT NULL)
    )
);
COMMENT ON COLUMN special_hours.special_id IS 'Surrogate primary key for the override row';
COMMENT ON COLUMN special_hours.location_id IS 'FK -> locations; facility this override applies to';
COMMENT ON COLUMN special_hours.calendar_date IS 'The specific date the override is in effect';
COMMENT ON COLUMN special_hours.opens_at IS 'Special opening time; NULL when is_closed';
COMMENT ON COLUMN special_hours.closes_at IS 'Special closing time; NULL when is_closed';
COMMENT ON COLUMN special_hours.is_closed IS 'TRUE if the facility is closed all day on this date';
COMMENT ON COLUMN special_hours.description IS 'Reason for the override (e.g. "Līgo (Midsummer Eve)")';
COMMENT ON CONSTRAINT unique_special_hours ON special_hours IS 'One override per location per date';
COMMENT ON CONSTRAINT check_special_times ON special_hours IS 'Times required only when not closed';


