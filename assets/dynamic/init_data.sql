CREATE TABLE IF NOT EXISTS spaces (
    level             VARCHAR(3)  NOT NULL,
    space_type        VARCHAR(20) NOT NULL CHECK (space_type IN ('STANDARD','OVERSIZED', 'EV')),
    amount            INT         NOT NULL,
    UNIQUE (level, space_type)
);
