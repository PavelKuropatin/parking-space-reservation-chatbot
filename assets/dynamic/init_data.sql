-- working_hours
CREATE TABLE working_hours (
    hours_id        INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    day_of_week     TEXT,
    opens_at        TIME,
    closes_at       TIME,
    is_24h          BOOLEAN  NOT NULL DEFAULT FALSE,
    is_closed       BOOLEAN  NOT NULL DEFAULT FALSE,
    CONSTRAINT unique_working_hours UNIQUE (day_of_week),
    CONSTRAINT check_hours_not_both CHECK (NOT (is_24h AND is_closed)),
    CONSTRAINT check_hours_times CHECK (
        (is_24h OR is_closed) AND opens_at IS NULL AND closes_at IS NULL
        OR
        (NOT is_24h AND NOT is_closed AND opens_at IS NOT NULL AND closes_at IS NOT NULL)
    )
);
COMMENT ON COLUMN working_hours.hours_id IS 'Surrogate primary key for the hours row';
COMMENT ON COLUMN working_hours.day_of_week IS 'ISO-8601 weekday: 1=Monday ... 7=Sunday';
COMMENT ON COLUMN working_hours.opens_at IS 'Opening time; NULL when is_24h or is_closed';
COMMENT ON COLUMN working_hours.closes_at IS 'Closing time; NULL when is_24h or is_closed';
COMMENT ON COLUMN working_hours.is_24h IS 'TRUE if open around the clock on this day';
COMMENT ON COLUMN working_hours.is_closed IS 'TRUE if fully closed on this day';


CREATE OR REPLACE VIEW vw_working_hours
AS
SELECT  wh.hours_id,
        wh.day_of_week, 
        CASE 
            WHEN wh.is_closed then null
            WHEN wh.is_24h THEN '00:00'
            ELSE wh.opens_at
        END as opens_at,
        CASE 
            WHEN wh.is_closed then null
            WHEN wh.is_24h THEN '23:59'
            ELSE wh.closes_at
        END as closes_at,
        wh.is_closed as temporaly_closed
FROM  working_hours wh
ORDER BY wh.day_of_week;


-- reservations
CREATE TABLE IF NOT EXISTS reservations (
    id                 SERIAL PRIMARY KEY,
    level              VARCHAR(3)      NOT NULL,
    space_type         VARCHAR(20)     NOT NULL,
    customer_full_name VARCHAR(100)    NOT NULL,
    license_plate      VARCHAR(20)     NOT NULL,
    start_datetime     TIMESTAMP       NOT NULL,
    end_datetime       TIMESTAMP       NOT NULL,
    total_cost         NUMERIC(10, 2),
    created_at         TIMESTAMP       NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT valid_period CHECK (start_datetime < end_datetime)
);


CREATE OR REPLACE VIEW vw_avaliable_spaces
AS
SELECT  s.level,
        s.space_type,
        s.amount - COUNT(r.id) AS available_spaces
FROM spaces s
LEFT JOIN reservations r
          ON r.space_type = s.space_type
             AND r.level = s.level 
             AND NOW() BETWEEN r.start_datetime AND r.end_datetime
GROUP BY s.level, s.space_type, s.amount
ORDER BY s.level, s.space_type;


CREATE OR REPLACE FUNCTION func_compute_total_cost()
RETURNS TRIGGER AS $$
DECLARE
    hourly_rate NUMERIC(8,2);
    daily_max   NUMERIC(8,2);
    current_day DATE;
    last_day    DATE;
    day_start   TIMESTAMP;
    day_end     TIMESTAMP;
    total_cost  NUMERIC(10,2) := 0;
BEGIN
    SELECT amount INTO hourly_rate FROM pricing WHERE price_type = 'hourly';
    SELECT amount INTO daily_max   FROM pricing WHERE price_type = 'daily_max';

    current_day := DATE(NEW.start_datetime);
    last_day    := DATE(NEW.end_datetime);

    WHILE current_day <= last_day LOOP
        day_start := GREATEST(NEW.start_datetime, current_day::TIMESTAMP);
        day_end := LEAST(NEW.end_datetime, (current_day + INTERVAL '1 day')::TIMESTAMP);

        total_cost := total_cost + LEAST(
            EXTRACT(EPOCH FROM (day_end - day_start)) / 3600.0 * hourly_rate,
            daily_max
        );

        current_day := current_day + 1;
    END LOOP;

    NEW.total_cost := ROUND(total_cost, 2);
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE OR REPLACE TRIGGER trigger_compute_total_cost 
BEFORE 
INSERT OR UPDATE
ON reservations
FOR EACH ROW
EXECUTE FUNCTION func_compute_total_cost();


